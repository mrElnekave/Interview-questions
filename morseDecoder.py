MORSE_CODE = {
".-" : "A",
"-...": "B",
"-.-.": "C",
"-..": "D",
".": "E",
"..-.": "F", "-----": "0",
"--.": "G", ".----": "1",
"....": "H", "..---": "2",
"..": "I", "...--": "3",
".---": "J", "....-": "4",
"-.-": "K", ".....": "5",
".-..": "L", "-....": "6",
"--": "M", "--...": "7",
"-.": "N", "---..": "8",
"---": "O", "----.": "9",
".--.": "P", ".-.-.-": ".",
"--.-": "Q", "--..--": ",",
".-.": "R", "..--..": "?",
"...": "S", "-....-": "-",
"-": "T", ".----.": "'",
"..-": "U", "---...": ":",
"...-": "V", ".-..-.": "'",
".--": "W", "-..-.": "/",
"-..-": "X", ".--.-.": "@",
"-.--": "Y",
"--..": "Z",
}


def decodeMorse(morse_code):
    morse_code = morse_code.split(' ')
    print(morse_code)
    output = ""

    for morse in morse_code:
        if morse == '':
            output = output.__add__(" ")
            continue
        output = output.__add__(MORSE_CODE[morse])
    return output


print(decodeMorse('.... . -.--   .--- ..- -.. .'))
